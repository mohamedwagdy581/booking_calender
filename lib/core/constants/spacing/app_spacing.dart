import 'package:flutter_screenutil/flutter_screenutil.dart';

// A consistent set of spacing values to be used across the app
abstract class AppSpacing {
  // --- PADDING & MARGINS ---
  static final double kHorizontalPadding = 16.w;
  static final double kVerticalPadding = 24.h;

  // --- SPACING ---
  static final double kSpaceXXS = 4.h;
  static final double kSpaceXS = 8.h;
  static final double kSpaceS = 12.h;
  static final double kSpaceM = 16.h;
  static final double kSpaceL = 24.h;
  static final double kSpaceXL = 32.h;
  static final double kSpaceXXL = 48.h;
}
